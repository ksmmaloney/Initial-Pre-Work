/*  A for loop can also iterate through the contents of an array. In this case--add the values of each element.

*/

var ourArr = [ 9, 10, 11, 12];
var ourTotal = 0;

for (var i = 0; i < ourArr.length; i++) {
  ourTotal += ourArr[i];
}

