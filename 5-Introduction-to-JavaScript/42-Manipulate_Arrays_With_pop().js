// You can change the data at the very end of an array with .pop(). When you use .pop(), the data pops off the end data and it lands inside a new variable that you've created. The element that gets popped off can be an array or a number or a string, any type of entry.


// Example
var ourArray = [1,2,3];
var removedFromOurArray = ourArray.pop();
// removedFromOurArray now equals 3, and ourArray now equals [1,2]

// Setup
var myArray = [["John", 23], ["cat", 2]];

// Only change code below this line.
var removedFromMyArray = myArray.pop();
