// Parameters are variables. Arguments are actual values. Parameters act as placeholders for the values that get put into a function when it's called. Functions are usually defined along with parameters. Arguments (values) are input (passed) into a function when it is called.

// Example
function ourFunctionWithArgs(a, b) {
  console.log(a - b);
}
ourFunctionWithArgs(10, 5); // Outputs 5

// Only change code below this line.
function functionWithArgs(a, b) {
  console.log(a + b);
}
functionWithArgs(2, 3);
