//  You can add data to the end of an array using " .push() "  You just add .push( ) directly after a variable name, and put a value inside the parentheses.  The function pushes one or more parameters onto the end of an array (but still inside the array end bracket).

// Example
var ourArray = ["Stimpson", "J", "cat"];
ourArray.push(["happy", "joy"]);
// ourArray now equals ["Stimpson", "J", "cat", ["happy", "joy"]]

