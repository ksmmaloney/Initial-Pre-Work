// You can use " unshift() " to add elements at the beginning of an array. It's similar to push, but it places elements at the beginning instead of the end.


//Example
var ourArray = ["Stimpson", "J", "cat"];
ourArray.shift(); // ourArray now equals ["J", "cat"]
ourArray.unshift("Happy");
// ourArray now equals ["Happy", "J", "cat"]

